        <h4>How to plan a wedding menu? What to expect on a wedding dinner menu?</h4>
        <p>Here are some of the most asked questions when it comes to planning a wedding or wedding reception menu..</p>
        <h5>What is usually on a wedding menu?</h5>
        <p>A traditional or conventional wedding menu has three courses, appetizer (soup/salad), main course and dessert  or a sweet dish, but ultimately it's your choice to add to, take away, or change things up! For example, if you really want to wow your guests, up the number of courses. Some weddings and events go up to twelve course servings and include various cuisines like Chinese, Lebanese, Indian, Mughalai, etc.</p>
       <h5>What is the most popular food at weddings?</h5>
        <p>Chicken, mutton, and vegetables including a few varieties of daal are the most commonly served main course items at Indian weddings as they can maintain their heat when served to large groups and all work well with Indian palates. This can vary from State to State as Bengali and Goan weddings are incomplete without a fish dish or two. Depending on religion  and other preferences other meats may be added, for eg. Muslim and Christian weddings may include beef dishes. In Goa and parts of the North-East, pork dishes are compulsory at Catholic weddings. North Indian weddings may include, Pani Poori, Chicken Butter Masala, Biryani, Jalebi, Gulab Jamun, Dal Makhni, Chicken Tikka, Malpua and such. Now-a-days, wedding buffets include cold cuts, salads (both veg & non-veg), noodles, fruit salads, fruit dishes and rolls.</p>
