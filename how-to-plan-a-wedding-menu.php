        <h5>Do weddings include fancy finger food?</h5>
        <p>While Canapes are not exactly on the menus of most Indian weddings, pre-wedding parties can include finger food like canapes. A small, prepared and usually decorative food, held in the fingers and often eaten in one bite could include tikkas, mini rolls, fish fingers, grilled or fried spiced potatoes and the like.</p>
       <h5>What about themed dinners and themed food?</h5>
        <p>Some hosts who want to make it different and even taste different will junk the regular menu and add food from another country. While this may work at destination weddings, the traditional Indian wedding is incomplete without the daal, roti, sabzi, butter chicken, jalebi and gulab jamun. However, modern couples may go for either Mexican, Spanish or French (or say Bengali or South-Indian) only cuisines for their weddings. Sometimes the entire theme, including the menu could be based on a particular locale or concept. Other couples may choose for example a pizza-only dinner with at least 10 varieties of pizza in slice form for the guests. Themed food weddings are more popular among younger couples who invite more of their office crowd, rather than senior citizens and family.</p>
      
       
