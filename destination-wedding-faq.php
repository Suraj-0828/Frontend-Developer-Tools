        <h5>Are destination weddings cheaper in India?</h5>
        <p>While destination weddings are perceived to be more expensive than local weddings, it may not always be the case. Since your guest list for such functions is cut drastically, including only close family and friends, you save on the costs that would have gone into a bigger local wedding with 800-1,000 people. If the host is not paying for travel and accommodation of the guests, destination weddings can be much cheaper than local wedding.</p>
       <h5>How much does a wedding in Goa cost?</h5>
        <p>It may round off to anything between Rs 15,000 to Rs 25,000 per person at the minimum, giving you a ballpark figure of Rs 15 Lacs to 20 Lacs. The venue may also charge you between Rs 75,000 to Rs 3,00,000 per event, depending on where you would like to organize your event, i.e., banquet halls, beaches, or lawns. Beach side, sea-facing venues are very expensive, particularly during the tourist season and the venue cost only could be in the range of Rs 5 lakh, not including food, entertainment and travel. The same is true for other tourist destinations in India like Kerala and Rajasthan (old palace locales)</p>
      
       
