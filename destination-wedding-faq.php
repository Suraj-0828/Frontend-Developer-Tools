        <H5>What is a destination wedding?</H5>
        <p>A destination wedding takes place or is held in a  scenic location away from the couple's hometown. Planning a destination wedding could be less expensive than planning a traditional wedding if it entails a smaller guest list or smaller reception party. The location can vary depending on the couple's budget, choice of food and other requirements.</p>
        <h5>Are destination weddings more expensive than locally held functions?</h5>
        <p>No. In fact if well planned in advance, destination weddings can be much cheaper than local weddings due to a reduced guest-list. Using credit card points or miles can help to save money on destination wedding travel costs.</p>
       <h5>How much does a destination marriage cost?</h5>
        <p>A destination wedding in metro cities of India can cost anywhere from Rs 2 lakhs to Rs 70 lakh depending on the venue and count of guests. It also depends if travel/transport and accommodation is being provided for all the guests. Beach side destination weddings in tourist locales like Goa, Kerala, etc., can start from Rs 20 lakh (for 100 guests) and can go upto Rs 90 lakh depending on the entertainment provided, menu and cuisine and other factors including season or off-season, 3 star or 5 star property, etc</p>
